export const getGradientFromId = (id: string) => {
  // hash the job id to get a colour
  const colour = colours[id.split('').reduce((a, b) => a + b.charCodeAt(0), 0) % colours.length];
  // another hash for the rotation of the gradient
  const rotation = id.split('').reduce((a, b) => a + b.charCodeAt(0), 0) % 360;
  // and finally, the hash for the graident intensity
  const intensity = Math.floor((id.split('').reduce((a, b) => a + b.charCodeAt(0), 0) % 50) + 25) / 100;
  return `linear-gradient(${rotation}deg, rgb(${colour[0]}, ${colour[1]}, ${colour[2]}, ${intensity}), rgb(${colour[0]}, ${colour[1]}, ${colour[2]}, 1))`;
};
